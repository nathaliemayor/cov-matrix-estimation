# ------------------------------------------------------------------------------
#                 WEIGHTS DISTRIBUTION
# ------------------------------------------------------------------------------

results_rdata$m240$weights %>% 
  dplyr::group_by(method) %>% 
  dplyr::summarise(
    sd = sd(value),
    median = median(value),
    min = min(value),
    max = max(value),
    q5 = quantile(value, probs = 0.05),
    q25 = quantile(value, probs = 0.25),
    q75 = quantile(value, probs = 0.75), 
    q95 = quantile(value, probs = 0.95)
  )

